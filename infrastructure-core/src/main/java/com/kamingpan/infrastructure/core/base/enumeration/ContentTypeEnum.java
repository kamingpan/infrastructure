    ContentTypeEnum(String format, String contentType) {
        this.format = format;
        this.contentType = contentType;
    }

    /**
     * 根据格式获取文件内容类型枚举
     *
     * @param format 格式
     * @return 文件内容类型枚举
     */
    public static ContentTypeEnum getByFormat(String format) {
        if (null != format && !format.trim().isEmpty()) {
            // 转换成小写
            format = format.toLowerCase();

            for (ContentTypeEnum contentTypeEnum : ContentTypeEnum.values()) {
                if (contentTypeEnum.getFormat().equals(format)) {
                    return contentTypeEnum;
                }
            }
        }

        return ContentTypeEnum.UN_KNOW;
    }

